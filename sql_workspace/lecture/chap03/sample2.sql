-- ------------------------------------------------------
-- (4) 문자 (처리)함수 - CEIL
-- ------------------------------------------------------ 
-- 주어진 숫자값보다 크거나 같은 최소 정수값을 반환
-- ------------------------------------------------------
SELECT
    ceil(10.6),
    ceil(-10.6)
FROM
    dual;
-- ------------------------------------------------------
-- (5) 문자 (처리)함수 - FLOOR 
-- ------------------------------------------------------ 
-- 주어진 숫자값보다 작거나 같은 최대 정수값을 반환
-- ------------------------------------------------------
SELECT
    floor(10),
    floor(10.6),
    floor(-10.6)
FROM
    dual;
-- ------------------------------------------------------
-- (6) 문자 (처리)함수 - SIGN
-- ------------------------------------------------------ 
-- 주어진 값이 양수인지 음수인지 0인지 식별할 수 있는 값을 반환
-- ------------------------------------------------------
-- SIGN 함수의 응용
SELECT employee_id, last_name, salary
FROM employees
-- WHERE sign(salary - 15000) = 1;
WHERE sign(salary - 15000) != -1; 
-- if salary > 15000 과 같음.
-- SIGN 으로 이상, 이하 만들려면 IN 함수 써서 0, 1 넣으면 됨. 혹은 위에 식에서 -1이 아닌걸로.
-- sign 함수는, 비교연산자를 대체할 수 있음(>,<,=,>=,<=)

